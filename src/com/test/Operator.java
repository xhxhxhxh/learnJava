package com.test;

public class Operator {
    public static void main(String[] args) {
        int a = 10;
        double b = 20.12;
        double c = b + a;
        float ee = 2.5F;
        int d = (int)(a + b);
        double e = (double)(a + ee);
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        System.out.println("e = " + e);
    }
}
